dias_da_semana = int(input('Digite um númemro para ver o dia da semana: '))

if dias_da_semana == 1:
    print('Domingo')

elif dias_da_semana == 2:
     print('Segunda-feira')

elif dias_da_semana == 3:
     print('Terça-feira')

elif dias_da_semana == 4:
     print('Quarta-feira')

elif dias_da_semana == 5:
     print('Quinta-feira')

elif dias_da_semana == 6:
     print('Sexta-feira')

elif dias_da_semana == 7:
     print('Sábado')

else:
    print('Valor inválido')